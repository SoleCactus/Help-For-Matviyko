from math import sqrt #імпортуємо потрібну функцію з модуля Math

#Вводимо катети
cat1 = float(input("Введіть перший катет: "))
cat2 = float(input("Введіть другий катет: "))

#Обробляємо отримані данні теоремою Піфагора
sum = cat1 ** 2 + cat2 ** 2
hip = sqrt(sum)

#Виводимо данні
print(f"Розмір гіпотенузи {hip}")